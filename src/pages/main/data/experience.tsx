const getAge = (date: string) => {
	var today = new Date();
	var birthDate = new Date(date);
	var age = today.getFullYear() - birthDate.getFullYear();
	var m = today.getMonth() - birthDate.getMonth();
	if (m < 0) return age - 1;
	if (m === 0 && today.getDate() < birthDate.getDate()) return age - 1;
	return age;
};

